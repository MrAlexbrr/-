# Исходные данные
money_capital = 20000  # Подушка безопасности
salary = 5000  # Ежемесячная зарплата
spend = 6000  # Траты за первый месяц
increase = 0.05  # Ежемесячный рост цен

# Для этой задачи лучше использовать цикл с условием, так как заранее неизвестно,
# сколько месяцев можно прожить, пока финансовая подушка не закончится.
# Мы будем отслеживать остаток денег и ежемесячно уменьшать его на расходы,
# которые увеличиваются на 5% каждый месяц.


# Подсчет количества месяцев
def months_without_debt(money_capital, salary, spend, increase):
    months = 0
    while money_capital >= 0:
        # Увеличиваем месяц
        months += 1

        # Вычитаем расходы из общего бюджета (подушка + зарплата)
        money_capital += salary  # добавляем зарплату
        money_capital -= spend  # вычитаем расходы

        # Если денег не хватает, завершаем цикл
        if money_capital < 0:
            months -= 1  # Убираем текущий месяц, так как в нем не хватает денег
            break

        # Увеличиваем расходы на заданный процент в следующем месяце
        spend *= (1 + increase)

    return months


# Вычисление
months = months_without_debt(money_capital, salary, spend, increase)

# Результат
print("Количество месяцев, которое можно протянуть без долгов:", months)

